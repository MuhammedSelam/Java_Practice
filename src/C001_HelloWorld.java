public class C001_HelloWorld {

    public static void main(String[] args) {

        System.out.println("Hello World");
        // Hello World

        System.out.println("\"Hello World\"");
        // "Hello World"

        System.out.println("Hello\nWorld");
        /*
        Hello
        World
         */

        System.out.println("Hello\tWorld");
        // Hello	World

    }
}
