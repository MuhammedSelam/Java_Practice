package day01;

public class C01_HelloJava {

    public static void main(String[] args) {

        System.out.println("Hello Java");
        // Hello Java

        System.out.println("\"Hello World\"");
        // "Hello World"

        System.out.println("Hello\nWorld");
        /*
        Hello
        World
         */

        System.out.println("Hello\tWorld");
        // Hello	World


    }

}
